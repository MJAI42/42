#include <unistd.h>

int	main (void)
{
	int i;
	int temp;

	i = 90;
	temp = 0;
	while (i > 64)
	{
		if (i % 2 == 0)
		{
			write (1, &i, 1);
			i--;
		}
		else if (i % 2 == 1)
		{
			temp = i + 32;
			write (1, &temp , 1);
			i--;
		}
		else
			i--;
	}
}